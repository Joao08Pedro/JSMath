// "Math.ceil()" arredonda para o valor mais alto

console.log("Math.ceil()");
console.log(Math.ceil(1.5));
console.log("=".repeat(50));

// """.repeat(Numero)" ele repete o quanto de vezes que você escolher
// "Math.floor()" arredonda para o valor mais baixo

console.log("Math.floor()");
console.log(Math.floor(1.5));
console.log("=".repeat(50));

// "Math.round()" arredonda de acordo com o numero pós a virgula

console.log("Math.round()");
console.log(Math.round(1.5));
console.log("=".repeat(50));

// "Math.pow()" soma um numero por quantas vezes ele mesmo (2, 3) = 2³

console.log("Math.pow()");
console.log(Math.pow(2, 3));
console.log("=".repeat(50));

// "Math.sqrt()" raiz quadrada

console.log("Math.sqrt()");
console.log(Math.sqrt(9));
console.log("=".repeat(50));

// "Math.random()" da um numero aleatório

console.log("Math.random()");
console.log(Math.random());
console.log("=".repeat(50));

